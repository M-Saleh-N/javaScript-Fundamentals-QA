// What is the difference between == and === in JavaScript?

// == checks if the values are equal, while === checks if the values are equal and the types are also equal.

console.log(1 == "1"); // true
// In the first example, the values are equal, but the types are different (number vs string)


console.log(1 === "1"); // false
// In the second example, the values are not equal because the types are different.


